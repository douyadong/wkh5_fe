class ListController extends Controller {
    constructor() {
        super();
        this.readyFun();
        this.choseFun();
    }

    /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
 条件选择的初始化函数
 -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
    readyFun(){
        $('.dic').hide();
        $('.bac').hide();
    }
    /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
 条件选择的区域选择的点击效果函数
 -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
     choseFun(){
        /* 区域选择点击事件*/
         $('#dic').click(function () {
             $('.dic').stop();
             $('.bac').stop();
             $(this).toggleClass('active-color-top');
             $('.dic').slideToggle();
             $('.bac').toggle();
         });
         /*区域与地铁选择点击事件*/
         $('.tabs > ul > li').click(function () {
            $(this).siblings().children('span').removeClass('active-color-dic');
             $(this).children('span').addClass('active-color-dic');
             let indexP = $(this).index();
             if (indexP == 0){
                 $('.metro-content').slideUp();
                 $('.dic-content').slideDown();
             }else if(indexP == 1){
                 $('.dic-content').slideUp();
                 $('.metro-content').slideDown();
             }
         })
     }


}


/*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
类的初始化
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
$(document).ready(function() {
    new ListController;
});
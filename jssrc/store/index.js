class IndexController extends Controller{
    constructor(){
        super();
        /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
        给第一个tabs-handle选项添加on样式
        -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/         
        $(".wk-tabs .tabs-handle li").first().addClass("on") ;
        /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
        载入组件逻辑
        -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/ 
        require([ "../components/bigdata.min"  ,"../components/tabs.min"],(BigData)=>{
            /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
            tabs实例化
            -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
            $(".wk-tabs").tabs({
                "fixedWhenScroll" : false ,
                "effect" : "fadeIn" ,
                "duration" : 200
            }) ;
        });
        /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
        给dom节点绑定事件
        -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
        this.addEventListener();
    }
    /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
    给dom节点绑定事件
    -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
    addEventListener(){
        // 页面滚动实现tab及以上固定;
        $(window).scroll(function(){
            if($(this).scrollTop()>=10){
                $('.hd').addClass('move');
                $('.tabs-handle').addClass('fixed');     
            }else{
                $('.hd').removeClass('move');
                $('.tabs-handle').removeClass('fixed');
            }
        });
        // 当点击tab是让固定定位消失;
        $(".wk-tabs .tabs-handle li").click(function(){
            // 让滚动top值为0;
            $(window).scrollTop('0');
        })
    }
}
/*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
类的初始化
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
$(document).ready(function() {
    new IndexController ;
}) ;